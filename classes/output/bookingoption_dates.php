namespace mod_booking\output;

use mod_booking\optiondates_handler;
use renderer_base;
use renderable;
use templatable;

/**
 * This file contains the definition for the renderable classes for bookingoption dates.
 *
 * @package     mod_booking
 * @copyright   2022 Wunderbyte GmbH {@link http://www.wunderbyte.at}
 * @author      Bernhard Fischer
 * @license     http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class bookingoption_dates implements renderable, templatable {

    /**
     * Constructor
     *
     * @param int $optionid
     */
    public function __construct(int $optionid) {

        $this->dates = optiondates_handler::get_existing_optiondates($optionid);
    }

    /**
     * Export for template.
     * @param renderer_base $output
     * @return array
     */
    public function export_for_template(renderer_base $output) {

        return array(
            'dates' => $this->dates
        );
    }
}
