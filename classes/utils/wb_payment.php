    /**
     * Decrypt a PRO license key to get the expiration date of the license
     *
     * @param string $encryptedlicensekey an object containing licensekey and signature
     * @return string the expiration date of the license key formatted as Y-m-d
     */
    public static function decryptlicensekey(string $encryptedlicensekey): string {
        global $CFG;
        // Step 1: Do base64 decoding.
        $encryptedlicensekey = base64_decode($encryptedlicensekey);

        // Step 2: Decrypt using public key.
        openssl_public_decrypt($encryptedlicensekey, $licensekey, self::MOD_BOOKING_PUBLIC_KEY);

        // Step 3: Do another base64 decode and decrypt using wwwroot.
        if (empty($licensekey)) {
            return false;
        }

        $c = base64_decode($licensekey);
        $ivlen = openssl_cipher_iv_length($cipher = "AES-128-CBC");
        $iv = substr($c, 0, $ivlen);

        // Bugfix when passing wrong license keys that are too short.
        if (strlen($iv) != 16) {
            return false;
        }

        $sha2len = 32;
        $ciphertextraw = substr($c, $ivlen + $sha2len);
        $decryptedcontent = openssl_decrypt($ciphertextraw, $cipher, $CFG->wwwroot, $options = OPENSSL_RAW_DATA, $iv);

        return $decryptedcontent;
    }

    /**
     * Split decrypted license content into expiration date and product token.
     *
     * License data format: "Y-m-d" (legacy Booking-only license) or "Y-m-d;product"
     * (product-bound license generated by the wb_license generator).
     *
     * @param mixed $decryptedcontent return value of decryptlicensekey()
     * @return array{expirationdate: string, product: string}
     */
    public static function parse_license_content($decryptedcontent): array {
        $parts = explode(';', trim((string)$decryptedcontent), 2);

        return [
            'expirationdate' => trim((string)($parts[0] ?? '')),
            'product' => isset($parts[1]) ? trim((string)$parts[1]) : '',
        ];
    }

    /**
     * Helper function to determine if the user has set a valid license key which has not yet expired.
     *
     * @return bool true if the license key is valid at current date
     * @throws \dml_exception
     */
    public static function pro_version_is_activated() {
        // Unit tests may force a specific PRO state, e.g. to simulate a plain
        // installation without Booking PRO (see override_pro_version_for_tests).
        if (self::$proversionoverride !== null && defined('PHPUNIT_TEST') && PHPUNIT_TEST) {
            return self::$proversionoverride;
        }
        // Get license key which has been set in settings.php.
        $pluginconfig = get_config('booking');
        if (!empty($pluginconfig->licensekey)) {
            $licensekeyfromsettings = $pluginconfig->licensekey;
            // DEBUG: echo "License key from plugin config: $licensekey_from_settings<br>"; END.

            $license = self::parse_license_content(self::decryptlicensekey($licensekeyfromsettings));
            // Booking PRO needs a plain Booking license (no product token, legacy)
            // or a combined Booking + Agent license. Agent-only keys ('wbagent')
            // do not unlock Booking PRO.
            if ($license['product'] === '' || $license['product'] === self::PRODUCT_BOOKING_AGENT) {
                $expirationtimestamp = strtotime($license['expirationdate']);
                // Return true if the current timestamp has not yet reached the expiration date.
                if ($expirationtimestamp !== false && time() < $expirationtimestamp) {
                    return true;
                }
            }
        }
        // Overriding - always use PRO for testing / debugging.
        // Check if Behat OR PhpUnit tests are running.
        if ((defined('BEHAT_SITE_RUNNING') && BEHAT_SITE_RUNNING) || (defined('PHPUNIT_TEST') && PHPUNIT_TEST)) {
            return true;
        }
        return false;
    }

    /**
     * Force pro_version_is_activated() to a fixed value in unit tests.
     *
     * Without this, PHPUnit runs always report the PRO version as activated,
     * so the code paths of a plain installation (no PRO) could never be tested.
     *
     * @param bool|null $value true/false to force the PRO state, null to remove the override
     * @return void
     * @throws \coding_exception if called outside of unit tests
     */
    public static function override_pro_version_for_tests(?bool $value): void {
        if (!defined('PHPUNIT_TEST') || !PHPUNIT_TEST) {
            throw new \coding_exception('override_pro_version_for_tests() must only be used in unit tests.');
        }
        self::$proversionoverride = $value;
    }
}
